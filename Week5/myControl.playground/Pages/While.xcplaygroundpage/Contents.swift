/*:
 # The While Statement
 ---
 
 ## Topic Essentials
 While statements are used to do something only when a specific condition exists. Once the condition changes the code will do something else.
 
 
 ### Objectives
 + Keep Bill Happy
 
 
 
 */

// While Loop
var billsHappyIndex = 0

while billsHappyIndex > 0 {
    billsHappyIndex -= 1
    print("Bill is getting crabby", billsHappyIndex)
}
// Fall out of the While Loop
print("Get Bill a drink")
billsHappyIndex = 10
print("Bill is happy now", billsHappyIndex)
